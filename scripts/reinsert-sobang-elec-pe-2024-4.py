print(f"과목: 객관식={sub_obj}, 단답형={sub_sa}, 주관식={sub_essay}")

# 기존 문제 모두 삭제
cur.execute("DELETE FROM questions WHERE exam_id = %s", (exam_id,))
deleted = cur.rowcount
conn.commit()
print(f"기존 문제 {deleted}개 삭제\n")

