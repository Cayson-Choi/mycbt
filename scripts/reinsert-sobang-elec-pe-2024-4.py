import sys, io, os
sys.stdout = io.TextIOWrapper(sys.stdout.buffer, encoding='utf-8')
import psycopg2
from dotenv import load_dotenv
load_dotenv('.env.local')

CAT_NAME = '소방설비산업기사(전기) 과정평가형'
YEAR, ROUND = 2024, 4
CODE_PREFIX = f'소방전기과정평가형-필기-{YEAR}-{ROUND}'

conn = psycopg2.connect(
    host=os.getenv('DATABASE_PGHOST_UNPOOLED') or os.getenv('DATABASE_PGHOST'),
    dbname=os.getenv('DATABASE_PGDATABASE','neondb'),
    user=os.getenv('DATABASE_PGUSER','neondb_owner'),
    password=os.getenv('DATABASE_PGPASSWORD'), sslmode='require',
)
cur = conn.cursor()

# 시험 / 과목 ID 조회
cur.execute("""SELECT e.id FROM exams e JOIN exam_categories c ON e.category_id = c.id
               WHERE c.name = %s AND e.year = %s AND e.round = %s AND e.exam_type = 'WRITTEN'""",
            (CAT_NAME, YEAR, ROUND))
exam_id = cur.fetchone()[0]
print(f"시험 id = {exam_id}")

cur.execute("SELECT id, name FROM subjects WHERE exam_id = %s", (exam_id,))
sub_map = {name: sid for sid, name in cur.fetchall()}
sub_obj = sub_map['객관식']
sub_sa = sub_map['단답형']
