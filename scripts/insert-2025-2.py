def crop_diagram(image_path):
    img = Image.open(image_path).convert('L')
    pixels = np.array(img)
    h, w = pixels.shape
    threshold = 200
    row_ink = np.sum(pixels < threshold, axis=1)
    min_ink = w * 0.005
    has_content = row_ink > min_ink
    content_started = False
    gap_start = -1
    gap_length = 0
    diagram_start = -1
    for i in range(h):
        if has_content[i]:
            content_started = True
            if gap_length >= 5 and gap_start > h * 0.1:
                diagram_start = i
                break
            gap_start = -1
            gap_length = 0
        else:
            if content_started:
                if gap_start == -1:
                    gap_start = i
                gap_length += 1
    if diagram_start <= 0 or diagram_start >= h * 0.9:
        return img
    margin = max(0, diagram_start - 3)
    diagram_bottom = h
    for i in range(h - 1, diagram_start, -1):
        if has_content[i]:
            diagram_bottom = min(h, i + 5)
            break
    return img.crop((0, margin, w, diagram_bottom))

