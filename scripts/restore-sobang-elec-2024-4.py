def Q(num, body, choices=None, answer=None, explain=None, q_type='객관식'):
    """문제 한 개 출력"""
    p = doc.add_paragraph()
    r = p.add_run(f'문제 {num}. ')
    r.bold = True
    r.font.size = Pt(12)
    p.add_run(body)

    if choices:
        marks = ['①', '②', '③', '④']
        for i, c in enumerate(choices):
            cp = doc.add_paragraph(style='List Bullet')
            cp.paragraph_format.left_indent = Pt(20)
            cp.text = ''
            cr = cp.add_run(f'   {marks[i]} {c}')
            cr.font.size = Pt(11)

    if answer is not None:
        ap = doc.add_paragraph()
        ar = ap.add_run(f'   ▶ 정답: {answer}')
        ar.bold = True
        ar.font.color.rgb = RGBColor(0xC0, 0x39, 0x2B)

    if explain:
        ep = doc.add_paragraph()
        er = ep.add_run(f'   해설: {explain}')
        er.font.size = Pt(10)
        er.font.color.rgb = RGBColor(0x55, 0x55, 0x55)

    doc.add_paragraph()  # 간격


